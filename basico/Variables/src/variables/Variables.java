package variables;

/**
 *
 * @author Fernando Cuatro 
 */
public class Variables {

 /**
  * Tenemos variables de tipo:
  * byte
  * short
  * int 
  * long
  * char
  * double
  * float
  * string
  * boolean
  * 
  * @param args 
  */

 public static void main(String[] args) {
  // Numeros enteros
  byte abc =5;
  short b = 200;
  int c = 100000000;
  long d = 1000000000000l;
  
  // Caracteres
  char e = 'a';

  // Decimales
  double f = 4.5;
  float g = 4.5f;
  
  // No es una variable primitida
  // Es una clase 
  // Pero se usa como variable
  String h = "Hola mundo";
  
  // El booleano
  boolean i = true; 
 }
 
}
